#include "ft_printf.h"

int	ft_ministr(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		ft_putstr_fd(("(null)"), 1);
		return (6);
	}
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
